public class _01_Variables {
    public static void main(String[] args) {



        /* Para declarar variablews se usa la siguiente sintaxis:
        *  tipo nombreVariable = valor*/

        int numero = 5;  // tipo (entero), nombreVariable (numero) = valor (5)
        String nombre = "Felipe"; //tipo (texto) , nombreVariable (nombre) = valor "felipe"

        System.out.println(numero); //impresion por pantalla de la variable numero
        System.out.println(nombre); //impresion por pantalla de la variable nombre
    }
}
